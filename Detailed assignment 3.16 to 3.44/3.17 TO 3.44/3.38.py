def abbreviation(day):
    if(day == "Monday"):
        return "M"
    elif(day == "Tuesday"):
      return "Tu"
    elif(day == "Wednesday"):
      return "W"
    elif(day == "Thursday"):
        return "Th"
    elif(day == "Friday"):
        return "F"
    elif(day == "Saturday"):
        return "Sa"
    elif(day == "Sunday"):
        return "Su"
    
